     private void cariData(String cariNama) {
    DefaultTableModel model = (DefaultTableModel) TabelUser.getModel();
    model.setRowCount(0);

    try (Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
         Statement stmt = con.createStatement()) {

        String query = "SELECT * FROM gaji WHERE nama LIKE '%" + cariNama + "%'";
        ResultSet rs = stmt.executeQuery(query);

        while (rs.next()) {
            String Nama = rs.getString("nama");
            String BulanTerima = rs.getString("bulan");
            String TahunTerima = rs.getString("tahun");
            model.addRow(new Object[]{Nama, BulanTerima, TahunTerima});
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
     }

    
