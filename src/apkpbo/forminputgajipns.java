   private void hitungTotal() {
    // Ambil nilai string dari setiap JTextField
    String gajiPokokStr = gajipokokField.getText().replace(",", "");
    String fungsionalStr = jabatanfield.getText().replace(",", "");
    String masaKerjaStr = kenaikanMasaKerjaField.getText().replace(",", "");
    String kinerjaStr = kinerjaField.getText().replace(",", "");
    String keluargaStr = keluargaField.getText().replace(",", "");
    String transportasiStr = transportasiField.getText().replace(",", "");
    String potonganStr = potonganField.getText().replace(",", "");
    
if (gajiPokokStr.isEmpty() || fungsionalStr.isEmpty() || masaKerjaStr.isEmpty() ||
            kinerjaStr.isEmpty() || keluargaStr.isEmpty() || transportasiStr.isEmpty()|| potonganStr.isEmpty()) {
            // Jika ada yang kosong, set totalField menjadi kosong juga
            totalField.setText("");
            return;
}
    // Konversi string ke tipe data numerik
    int gajiPokok = Integer.parseInt(gajiPokokStr);
    int fungsional = Integer.parseInt(fungsionalStr);
    int masaKerja = Integer.parseInt(masaKerjaStr);
    int kinerja = Integer.parseInt(kinerjaStr);
    int keluarga = Integer.parseInt(keluargaStr);
    int transportasi = Integer.parseInt(transportasiStr);
    int potongan = Integer.parseInt(potonganStr);

    // Hitung total
    int total = gajiPokok + fungsional + masaKerja + kinerja + keluarga + transportasi - potongan;

    // Format angka total dan setel nilai ke totalField
    DecimalFormat df = new DecimalFormat("#,###");
    String formattedTotal = df.format(total);
    totalField.setText(formattedTotal);
}
     

