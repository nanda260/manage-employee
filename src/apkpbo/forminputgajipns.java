    private void frekuensiComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_frekuensiComboItemStateChanged
        String gajiPokokStr = gajipokokField.getText().replace(",", "");
        String keterlambatan = (String) keterlambatanCombo.getSelectedItem();
        String frekuensi = (String) frekuensiCombo.getSelectedItem();
        int gajiPokok = Integer.parseInt(gajiPokokStr);

        int potongan = 0;
        if (keterlambatan.equals("1-30 menit")) {
            if (frekuensi.equals("1 kali")) {
                potongan = (int) (0.05 * gajiPokok);
            } else if (frekuensi.equals("2 kali")) {
                potongan = (int) (0.07 * gajiPokok);
            } else if (frekuensi.equals(">2 kali")) {
                potongan = (int) (0.09 * gajiPokok);
            }
        } else if (keterlambatan.equals("31-60 menit")) {
            if (frekuensi.equals("1 kali")) {
                potongan = (int) (0.10 * gajiPokok);
            } else if (frekuensi.equals("2 kali")) {
                potongan = (int) (0.15 * gajiPokok);
            } else if (frekuensi.equals(">2 kali")) {
                potongan = (int) (0.19 * gajiPokok);
            }
        } else if (keterlambatan.equals(">60 menit")) {
            if (frekuensi.equals("1 kali")) {
                potongan = (int) (0.20 * gajiPokok);
            } else if (frekuensi.equals("2 kali")) {
                potongan = (int) (0.25 * gajiPokok);
            } else if (frekuensi.equals(">2 kali")) {
                potongan = (int) (0.29 * gajiPokok);
            }
        } else if (keterlambatan.equals("Tidak pernah")) {
            if (frekuensi.equals("1Tidak pernah")) {
                potongan = (int) (0 * gajiPokok);
            }
        }

        // Mengatur nilai potongan dengan format ribuan
        DecimalFormat df = new DecimalFormat("#,###");
        String formattedPotongan = df.format(potongan);

        // Mengatur nilai potongan di potonganField
        potonganField.setText(formattedPotongan);
    }//GEN-LAST:event_frekuensiComboItemStateChanged

