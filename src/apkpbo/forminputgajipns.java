    private void inputgajiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputgajiButtonActionPerformed
        String Nama, GolonganPNS, JabatanFungsional, MasaKerja, Keterlambatan, Frekuensi, Bulan;
        String Tahun, GajiPokok, TunjanganKinerja, TunjanganJabatanFungsional, TunjanganKeluarga, TunjanganTransportasi, KenaikanMasaKerja, TotalPotongan, TotalPendapatan;

        String SUrl = "jdbc:mysql://localhost:3306/aplikasi_database";
        String SUser = "root";
        String SPass = "";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            
            Nama = namaField.getText();
            GolonganPNS = golonganCombo.getSelectedItem().toString();
            Bulan = bulanField.getSelectedItem().toString();
            JabatanFungsional = fungsionalCombo.getSelectedItem().toString();
            MasaKerja = masakerjaSpin.getValue().toString();
            Keterlambatan = keterlambatanCombo.getSelectedItem().toString();
            Frekuensi = frekuensiCombo.getSelectedItem().toString();
            Tahun = tahunField.getText();
            GajiPokok = gajipokokField.getText();
            TunjanganKinerja = kinerjaField.getText();
            TunjanganJabatanFungsional = jabatanfield.getText();
            TunjanganKeluarga = keluargaField.getText();
            TunjanganTransportasi = transportasiField.getText();
            KenaikanMasaKerja = kenaikanMasaKerjaField.getText();
            TotalPotongan = potonganField.getText();
            TotalPendapatan = totalField.getText();
            
            String query = "INSERT INTO gaji (nama, golongan_pns, jabatan_fungsional, masa_kerja, keterlambatan, frekuensi, tahun, gaji_pokok, tunjangan_kinerja, tunjangan_jabatan_fungsional, tunjangan_keluarga, tunjangan_transportasi, kenaikan_masa_kerja, total_potongan, total_pendapatan, bulan) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(query);

            ps.setString(1, Nama);
            ps.setString(2, GolonganPNS);
            ps.setString(3, JabatanFungsional);
            ps.setString(4, MasaKerja);
            ps.setString(5, Keterlambatan);
            ps.setString(6, Frekuensi);
            ps.setString(7, Tahun);
            ps.setString(8, GajiPokok);
            ps.setString(9, TunjanganKinerja);
            ps.setString(10, TunjanganJabatanFungsional);
            ps.setString(11, TunjanganKeluarga);
            ps.setString(12, TunjanganTransportasi);
            ps.setString(13, KenaikanMasaKerja);
            ps.setString(14, TotalPotongan);
            ps.setString(15, TotalPendapatan);
            ps.setString(16, Bulan);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data Gaji berhasil ditambahkan!");

            inputgaji InputGajiFrame = new inputgaji();
            InputGajiFrame.setUserDetails(nameValue.getText(), emailValue.getText(), passValue.getText());
            InputGajiFrame.setVisible(true);
            InputGajiFrame.pack();
            InputGajiFrame.setLocationRelativeTo(null);
            this.dispose();     
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
            JOptionPane.showMessageDialog(new JFrame(), "Error: " + e.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_inputgajiButtonActionPerformed

